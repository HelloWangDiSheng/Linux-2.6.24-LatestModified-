#include <linux/bitops.h>
#include <linux/module.h>
#include <asm/types.h>
#include <asm/byteorder.h>

/*nr所需字长数目*/
#define BITOP_WORD(nr)		((nr) / BITS_PER_LONG)

/*查询内存区域[addr,addr+size]中，子区间[addr+offset, addr+size]中第一个置位的比特位索引*/
unsigned long find_next_bit(const unsigned long *addr, unsigned long size, unsigned long offset)
{
	/*包含起始位offset的字长地址，如在IA-32上，假设offset为100，则前三个字长96位直接跳过，
	定位到第四个字长的起始位置，(unsigned long *)addr+3*/
	const unsigned long *p = addr + BITOP_WORD(offset);
	/*将偏移位对字长对齐，以上例就是100的低5位全部清零，0x64就是1100100，对齐后就是0x60*/
	unsigned long result = offset & ~(BITS_PER_LONG-1);
	unsigned long tmp;
	/*查询起始位置不在范围内，直接退出*/
	if (offset >= size)
		return size;
	size -= result;
	offset %= BITS_PER_LONG;
	if (offset) 
	{
		tmp = *(p++);
		tmp &= (~0UL << offset);
		if (size < BITS_PER_LONG)
			goto found_first;
		if (tmp)
			goto found_middle;
		size -= BITS_PER_LONG;
		result += BITS_PER_LONG;
	}
	while (size & ~(BITS_PER_LONG-1)) {
		if ((tmp = *(p++)))
			goto found_middle;
		result += BITS_PER_LONG;
		size -= BITS_PER_LONG;
	}
	if (!size)
		return result;
	tmp = *p;

found_first:
	tmp &= (~0UL >> (BITS_PER_LONG - size));
	if (tmp == 0UL)		/* Are any bits set? */
		return result + size;	/* Nope. */
found_middle:
	return result + __ffs(tmp);
}

EXPORT_SYMBOL(find_next_bit);

/*查找地址空间中[addr,addr+size]中[offset,size]区间内第一个未置位的比特位编号*/
unsigned long find_next_zero_bit(const unsigned long *addr, unsigned long size, unsigned long offset)
{
	const unsigned long *p = addr + BITOP_WORD(offset);
	unsigned long result = offset & ~(BITS_PER_LONG-1);
	unsigned long tmp;

	if (offset >= size)
		return size;
	size -= result;
	offset %= BITS_PER_LONG;
	if (offset) {
		tmp = *(p++);
		tmp |= ~0UL >> (BITS_PER_LONG - offset);
		if (size < BITS_PER_LONG)
			goto found_first;
		if (~tmp)
			goto found_middle;
		size -= BITS_PER_LONG;
		result += BITS_PER_LONG;
	}
	while (size & ~(BITS_PER_LONG-1)) {
		if (~(tmp = *(p++)))
			goto found_middle;
		result += BITS_PER_LONG;
		size -= BITS_PER_LONG;
	}
	if (!size)
		return result;
	tmp = *p;

found_first:
	tmp |= ~0UL << size;
	if (tmp == ~0UL)	/* Are any bits zero? */
		return result + size;	/* Nope. */
found_middle:
	return result + ffz(tmp);
}

EXPORT_SYMBOL(find_next_zero_bit);

